
public class TestandoCastings {

	public static void main(String[] args) {
	int C = 10;
	int D = 5;
	int soma = C + D;
	double divisao = (double) C/(double) D;
	System.out.println("O valor de C + D é = " + soma);
	System.out.println("O valor de C/D é = " + divisao);
	
	char A = 'A';
	char Z = 'Z';
	int i;
	i = A + Z;
	System.out.println("O valor de A + Z é = " + i); //retorna o valor da soma de cada letra 	
	}

}
